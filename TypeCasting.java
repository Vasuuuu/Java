public class TypeCasting{
     public static void main(Strings[] args){
       //Widening Casting
         int myInt = 9;
         double myDouble = myInt; 

    System.out.println(myInt);      
    System.out.println(myDouble);   
  
   /* Narrowing Casting
      double myDouble = 9.78d;
      int myInt = (int) myDouble;

      System.out.println(myDouble);  
      System.out.println(myInt);  */
  }
}
